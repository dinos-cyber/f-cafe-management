package Person;

/**
 *
 * @author Admin
 */
public class Person {
    private String name;
    private int yob;

    public Person(String name, int yob) {
        this.name = name;
        this.yob = yob;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYob() {
        return yob;
    }

    public void setYob(int yob) {
        this.yob = yob;
    }
    
    
    
}
